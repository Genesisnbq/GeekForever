package _01_Java_Base;

public class _02_MathCalc { // 用大括号括起来的都是代码块
    public static void main(String[] args) { // 0个或n个就是代码块,
        System.out.println(5 + 6); // 可以去掉引号, 然后 a+b  输出a+b的结果
        System.out.println(5 - 6);
        System.out.println(5 * 6);
        System.out.println(5 / 6); //整除
        int x = 1, y = 2; //variable 变量
        int c = x * y;
        System.out.println(c); // 语句的标志就是一个分号
    }

}
